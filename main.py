def realizar_evaluacion():
    """
    Función principal para realizar la evaluación del usuario.
    """
    print("\nEVALUACIÓN DE RIESGO DIGITAL")
    print("-" * 60)

    nombre = input("Ingrese su nombre: ")

    while nombre.strip() == "":
        print("El nombre no puede estar vacío.")
        nombre = input("Ingrese su nombre: ")

    preguntas = obtener_preguntas()
    puntaje_total = 0
    respuestas_usuario = []

    print(f"\nHola, {nombre}. Responde las siguientes preguntas:")
    print("Selecciona una opción del 1 al 3.\n")

    for i, item in enumerate(preguntas, start=1):
        print(f"{i}. {item['pregunta']}")

        for numero_opcion, opcion in enumerate(item["opciones"], start=1):
            texto_opcion = opcion[0]
            print(f"   {numero_opcion}. {texto_opcion}")

        respuesta = validar_opcion("Respuesta: ", 1, 3)

        texto_respuesta = item["opciones"][respuesta - 1][0]
        puntaje = item["opciones"][respuesta - 1][1]

        puntaje_total += puntaje

        respuestas_usuario.append({
            "pregunta": item["pregunta"],
            "respuesta": texto_respuesta,
            "puntaje": puntaje
        })

        print()

    nivel, interpretacion = clasificar_riesgo(puntaje_total)
    recomendacion = generar_recomendacion(nivel)

    mostrar_resultado(nombre, puntaje_total, nivel, interpretacion, recomendacion)

    guardar_reporte(nombre, puntaje_total, nivel, interpretacion, recomendacion, respuestas_usuario)


def clasificar_riesgo(puntaje):
    """
    Clasifica el riesgo digital según el puntaje obtenido.
    Puntaje mínimo: 8
    Puntaje máximo: 24
    """
    if puntaje >= 8 and puntaje <= 13:
        nivel = "Riesgo alto"
        interpretacion = "El usuario presenta hábitos digitales inseguros y necesita mejorar su protección."
    elif puntaje >= 14 and puntaje <= 19:
        nivel = "Riesgo medio"
        interpretacion = "El usuario tiene algunos hábitos seguros, pero todavía debe fortalecer su seguridad digital."
    else:
        nivel = "Riesgo bajo"
        interpretacion = "El usuario demuestra buenas prácticas de seguridad digital."

    return nivel, interpretacion


def generar_recomendacion(nivel):
    """
    Genera recomendaciones personalizadas según el nivel de riesgo.
    """
    if nivel == "Riesgo alto":
        recomendacion = (
            "Se recomienda utilizar contraseñas seguras, activar la verificación en dos pasos, "
            "evitar abrir enlaces desconocidos, no descargar archivos sospechosos y proteger la "
            "información personal en redes sociales."
        )
    elif nivel == "Riesgo medio":
        recomendacion = (
            "Se recomienda reforzar los hábitos de seguridad digital, revisar los permisos de las "
            "aplicaciones, verificar la información obtenida en internet o mediante inteligencia "
            "artificial y evitar compartir datos sensibles."
        )
    else:
        recomendacion = (
            "El usuario mantiene buenos hábitos digitales. Se recomienda continuar protegiendo sus "
            "datos, actualizar contraseñas periódicamente y mantenerse informado sobre nuevos riesgos "
            "tecnológicos."
        )

    return recomendacion


