def validar_opcion(mensaje, minimo, maximo):
    """
    Función que valida que el usuario ingrese un número dentro del rango permitido.
    """
    while True:
        try:
            opcion = int(input(mensaje))

            if minimo <= opcion <= maximo:
                return opcion
            else:
                print(f"Error: ingrese una opción entre {minimo} y {maximo}.")

        except ValueError:
            print("Error: debe ingresar un número válido.")


