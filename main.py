def guardar_reporte(nombre, puntaje, nivel, interpretacion, recomendacion, respuestas_usuario):
    """
    Guarda un reporte básico en un archivo de texto.
    Esta funcionalidad permite evidenciar mejor el resultado del sistema.
    """
    nombre_archivo = "reporte_ciberescudo.txt"

    with open(nombre_archivo, "w", encoding="utf-8") as archivo:
        archivo.write("REPORTE DE EVALUACIÓN - CIBERESCUDO IA\n")
        archivo.write("=" * 60 + "\n")
        archivo.write(f"Usuario evaluado: {nombre}\n")
        archivo.write(f"Puntaje obtenido: {puntaje}/24\n")
        archivo.write(f"Nivel de seguridad digital: {nivel}\n\n")

        archivo.write("Interpretación:\n")
        archivo.write(interpretacion + "\n\n")

        archivo.write("Recomendación personalizada:\n")
        archivo.write(recomendacion + "\n\n")

        archivo.write("Detalle de respuestas:\n")
        archivo.write("-" * 60 + "\n")

        for i, respuesta in enumerate(respuestas_usuario, start=1):
            archivo.write(f"{i}. {respuesta['pregunta']}\n")
            archivo.write(f"Respuesta: {respuesta['respuesta']}\n")
            archivo.write(f"Puntaje: {respuesta['puntaje']}\n\n")

    print(f"\nSe generó el archivo '{nombre_archivo}' con el reporte de la evaluación.")


